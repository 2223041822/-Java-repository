package www.xiaojin.search.basicsearch;

public class Demo1 {
    public static void main(String[] args) {
        System.out.println(true & true);//并且
        System.out.println(false & true);
        System.out.println(true & false);
        System.out.println(false & false);
//        &为且｜为与
//       ^逻辑异或相同为false不同为true
//        ！逻辑非，取反
//        ｜｜短路逻辑或符，&&短路逻辑与
//        ｜｜第一个为true就为true
//        &&第一个为false就为false
    }
}
